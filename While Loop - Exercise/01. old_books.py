searched_book = input()

counter = 0
is_found = False
curr_book = input()

while curr_book != "No More Books":
    if searched_book == curr_book:
        is_found = True
        print(f"You checked {counter} books and found it.")
        break

    counter += 1
    curr_book = input()

if not is_found:
    print("The book you search is not here!")
    print(f"You checked {counter} books.")
